//////////////////////////////////////////////////////////////////////////////////////////
void Keyword::SaveImp
//////////////////////////////////////////////////////////////////////////////////////////
// Save
// 
(
 WDAFile &o // 
)
//////////////////////////////////////////////////////////////////////////////////////////
{
    o.Write( (DWORD)kwtype );
    o.Write( (DWORD)rel );
    o.Write( txt );
    o.Write( (DWORD)keywords.size() );
    list< string >::iterator i;
    for( i = keywords.begin(); i != keywords.end(); i++ ){
        o.Write( (*i) );        
    }

    ScriptContainer::SaveImp( o );
}
//////////////////////////////////////////////////////////////////////////////////////////
void Keyword::LoadImp
//////////////////////////////////////////////////////////////////////////////////////////
// Load
// 
(
 WDAFile &i, // 
 DWORD ver
)
//////////////////////////////////////////////////////////////////////////////////////////
{
    DWORD size;
    i.Read( (DWORD &)kwtype );
    i.Read( (DWORD &)rel );
    i.Read( txt );
    i.Read( size );    
    DWORD z;
    for( z = 0; z != size; z++ ){
        string kw;
        i.Read( kw );
        keywords.push_back( kw );
    }
    if( kwtype == Intro ){
        SetName( "Intro Text" );
        SetDisplayName( "Intro Text" );
    }else if( kwtype == End ){
        SetName( "Default Text" );
        SetDisplayName( "Default Text" );
    }else if( kwtype == Death ){
		SetName( "OnDeath" );
		SetDisplayName( "OnDeath" );
	}else if( kwtype == Bye ){
		SetName( "Bye" );
		SetDisplayName( "Bye" );
	}else{
        UpdateName();
    }

    ScriptContainer::LoadImp( i, ver );
}

