//////////////////////////////////////////////////////////////////////////////////////////
void Keyword::UpdateName( void )
//////////////////////////////////////////////////////////////////////////////////////////
// Updates the keyword's name
// 
//////////////////////////////////////////////////////////////////////////////////////////
{
    if( keywords.empty() ){
        name = "<undefined>";
        displayName = "<undefined>";
    }else{
        name = "";
        list< string >::iterator i;
        for( i = keywords.begin(); i != keywords.end(); ){
            name += (*i);
            i++;
            if( i != keywords.end() ){
                name += ", ";
            }
        }
        displayName = name;
    }
}

